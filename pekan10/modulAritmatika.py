import math

def tambah(bil1, bil2):
    hasil = bil1+bil2
    return hasil

def kurang(bil1, bil2):
    hasil = bil1-bil2
    return hasil
    
def kali(bil1, bil2):
    hasil = bil1 * bil2
    return hasil
   
def bagi(bil1, bil2):
    hasil = bil1 / bil2
    return hasil

def pangkat(bil1, bil2):
    hasil = math.pow(bil1, bil2)
    return hasil
    